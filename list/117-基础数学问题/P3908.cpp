ll n;
ll ans;


void solve()
{
    scanf("%lld", &n);
    if(n % 4 == 1) ans = 1;
    if(n % 4 == 2) ans = n + 1;
    if(n % 4 == 3) ans = 0;
    if(n % 4 == 0) ans = n;

    printf("%lld\n", ans);
}

